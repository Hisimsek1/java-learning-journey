public class MathSinifi {
    public static void main(String[] args) {
       
// Math sınıfı metodları
// Math.ceil(x) => bir büyük tam sayıya yuvarlama yapar.
// Math.cos(x) => cos değerini döner
// Math.sin(x) => sin değerini döner
// Math.floor(x) => küçük tam sayıya yuvarlar
// Math.max(x,y) => büyük sayıyı verir
// Math.min(x,y) => küçük sayıyı verir
// Math.pow(3,5) => 3^5 üs alır
// Math.random() => 1 ve 0 arasında rastgele değer
// Math.round(x) => yakın olan tam sayıya döner
// Math.abs(x) => mutlak değer
System.out.println(Math.ceil(1.1));
System.out.println(Math.cos(30));
System.out.println(Math.floor(1.1));
System.out.println(Math.max(5, 4));
System.out.println(Math.pow(2, 3));
System.out.println(Math.random());
System.out.println(Math.round(7.8));
System.out.println(Math.abs(-5));
System.out.println(Math.sqrt(16));

    }
}
